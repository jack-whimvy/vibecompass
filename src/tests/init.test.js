test('runCli sync-agents creates and updates managed agent instruction files', async () => {
  const tempDir = await mkdtemp(path.join(os.tmpdir(), 'vibecompass-sync-agents-'));
  const rootDir = path.join(tempDir, '.compass');
  const stdout = [];
  const stderr = [];

  try {
    await initializeProjectMemory({
      cwd: tempDir,
      rootDir,
      name: 'Agent File Project',
      description: 'Keeps agent files generated from project memory.',
      mode: 'local-only',
      repos: [{ id: 'app', remote: 'https://github.com/example/app.git' }],
    });

    const dryRunExitCode = await runCli(
      ['sync-agents', '--root', '.compass', '--dry-run'],
      {
        stdout: {
          write(chunk) {
            stdout.push(chunk);
          },
        },
        stderr: {
          write() {},
        },
      },
      { cwd: tempDir },
    );

    await assert.rejects(() => access(path.join(tempDir, 'CLAUDE.md')));
    assert.equal(dryRunExitCode, 0);
    assert.match(stdout.join(''), /CLAUDE\.md: dry-run-create/);

    stdout.length = 0;
    const exitCode = await runCli(
      ['sync-agents', '--root', '.compass'],
      {
        stdout: {
          write(chunk) {
            stdout.push(chunk);
          },
        },
        stderr: {
          write() {},
        },
      },
      { cwd: tempDir },
    );

    const claude = await readFile(path.join(tempDir, 'CLAUDE.md'), 'utf8');
    const cursorRules = await readFile(path.join(tempDir, '.cursorrules'), 'utf8');
    const copilot = await readFile(path.join(tempDir, '.github/copilot-instructions.md'), 'utf8');

    assert.equal(exitCode, 0);
    assert.match(claude, /vibecompass:start - managed by VibeCompass/);
    assert.match(claude, /Agent File Project Claude Instructions/);
    assert.match(claude, /compact bootloader/);
    assert.match(claude, /planning mode/);
    assert.match(claude, /vibecompass end-session/);
    assert.match(claude, /tool-specific Current session blocks are continuity summaries/);
    assert.match(cursorRules, /Agent File Project Cursor Rules/);
    assert.match(copilot, /Agent File Project Copilot Instructions/);

    await writeFile(
      path.join(tempDir, 'CLAUDE.md'),
      `User header\n\n${claude}\nUser footer\n`,
      'utf8',
    );

    await runCli(
      ['sync-agents', '--root', '.compass', '--format', 'claude_md'],
      {
        stdout: {
          write() {},
        },
        stderr: {
          write() {},
        },
      },
      { cwd: tempDir },
    );

    const updatedClaude = await readFile(path.join(tempDir, 'CLAUDE.md'), 'utf8');
    assert.match(updatedClaude, /^User header/);
    assert.match(updatedClaude, /User footer\s*$/);

    await writeFile(path.join(tempDir, 'AGENTS.md'), 'Hand-written agents file\nReview handoff locally.\n', 'utf8');
    stdout.length = 0;
    await runCli(
      ['sync-agents', '--root', '.compass', '--format', 'agents_md'],
      {
        stdout: {
          write(chunk) {
            stdout.push(chunk);
          },
        },
        stderr: {
          write(chunk) {
            stderr.push(chunk);
          },
        },
      },
      { cwd: tempDir },
    );

    assert.match(stderr.join(''), /AGENTS\.md: warning/);
    assert.equal(await readFile(path.join(tempDir, 'AGENTS.md'), 'utf8'), 'Hand-written agents file\nReview handoff locally.\n');

    stdout.length = 0;
    stderr.length = 0;
    await runCli(
      ['sync-agents', '--root', '.compass', '--format', 'agents_md', '--adopt-existing'],
      {
        stdout: {
          write(chunk) {
            stdout.push(chunk);
          },
        },
        stderr: {
          write(chunk) {
            stderr.push(chunk);
          },
        },
      },
      { cwd: tempDir },
    );

    const adoptedAgents = await readFile(path.join(tempDir, 'AGENTS.md'), 'utf8');
    assert.match(stdout.join(''), /AGENTS\.md: adopt/);
    assert.match(stderr.join(''), /possible workflow overlap on lines 2/);
    assert.match(adoptedAgents, /^Hand-written agents file/);
    assert.match(adoptedAgents, /vibecompass:start - managed by VibeCompass/);
    assert.equal((adoptedAgents.match(/vibecompass:start/g) ?? []).length, 1);

    stdout.length = 0;
    await runCli(
      ['sync-agents', '--root', '.compass', '--format', 'agents_md', '--adopt-existing'],
      {
        stdout: {
          write(chunk) {
            stdout.push(chunk);
          },
        },
        stderr: {
          write() {},
        },
      },
      { cwd: tempDir },
    );

    const readoptedAgents = await readFile(path.join(tempDir, 'AGENTS.md'), 'utf8');
    assert.match(stdout.join(''), /AGENTS\.md: update/);
    assert.equal((readoptedAgents.match(/vibecompass:start/g) ?? []).length, 1);
    assert.equal(readoptedAgents, adoptedAgents);

    stdout.length = 0;
    await runCli(
      ['status', '--root', '.compass'],
      {
        stdout: {
          write(chunk) {
            stdout.push(chunk);
          },
        },
        stderr: {
          write() {},
        },
      },
      { cwd: tempDir },
    );

    assert.match(stdout.join(''), /AGENTS\.md: current/);
    assert.doesNotMatch(stdout.join(''), /AGENTS\.md: dry-run-update/);
  } finally {
    await rm(tempDir, { recursive: true, force: true });
  }
});

